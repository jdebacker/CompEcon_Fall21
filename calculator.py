def sum_two(a,b):
    '''
    Arg:
        a,b: two int
    
    Returns:
        c: int that equals to the sum of two arguments
    '''
    c = a + b
    return c

def pdt_two(e,f):
    '''
    Arg:
        e,f: two int
    
    Returns:
        g: int that equals to the sum of two arguments
    '''
    g = e * f
    return g